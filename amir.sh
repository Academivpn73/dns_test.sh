# ---------- Ping Function ----------
get_ping() {
    local dns=$1
    local result
    result=$(ping -c 1 -W 1 "$dns" 2>/dev/null | grep 'time=' | awk -F'time=' '{print $2}' | cut -d' ' -f1)
    if [ -z "$result" ]; then
        echo "timeout"
    else
        echo "${result} ms"
    fi
}

